-- ==========================================
-- 1. CREACIÓN DE TABLAS DE CATÁLOGOS
-- ==========================================

CREATE EXTENSION IF NOT EXISTS pgcrypto;

CREATE TYPE log_status AS ENUM ('PENDING', 'SUCCESS', 'FAILED');

-- Tabla de Categorías (Sports, Finance, Movies)
CREATE TABLE categories (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) UNIQUE NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- Tabla de Canales (SMS, E-Mail, Push)
CREATE TABLE channels (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) UNIQUE NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- ==========================================
-- 2. CREACIÓN DE TABLAS PRINCIPALES
-- ==========================================

-- Tabla de Usuarios (Mocks)
CREATE TABLE users (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    email VARCHAR(150) UNIQUE NOT NULL,
    phone_number VARCHAR(20) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- ==========================================
-- 3. CREACIÓN DE TABLAS PIVOTE (N:M)
-- ==========================================

-- Tabla pivote: Usuarios y Categorías a las que están suscritos
CREATE TABLE user_subscriptions (
    user_id UUID REFERENCES users(id) ON DELETE CASCADE,
    category_id INTEGER REFERENCES categories(id) ON DELETE CASCADE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (user_id, category_id)
);

-- Tabla pivote: Usuarios y sus Canales preferidos de notificación
CREATE TABLE user_channels (
    user_id UUID REFERENCES users(id) ON DELETE CASCADE,
    channel_id INTEGER REFERENCES channels(id) ON DELETE CASCADE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (user_id, channel_id)
);

-- ==========================================
-- 4. TABLA DE REGISTROS (LOGS)
-- ==========================================

-- Tabla de Historial de Notificaciones
CREATE TABLE notification_logs (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    message TEXT NOT NULL,
    category_id INTEGER REFERENCES categories(id) ON DELETE SET NULL,
    channel_id INTEGER REFERENCES channels(id) ON DELETE SET NULL,
    user_id UUID REFERENCES users(id) ON DELETE CASCADE,
    status log_status NOT NULL DEFAULT 'PENDING',
    error_message TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- ==========================================
-- 5. ÍNDICES DE RENDIMIENTO (Performance Tuning)
-- ==========================================
-- Como el sistema buscará a los usuarios por categoría, necesitamos un índice aquí.
CREATE INDEX idx_user_subs_category ON user_subscriptions(category_id);
-- Índice para recuperar rápidamente el historial ordenado por fecha (de más nuevo a más viejo)
CREATE INDEX idx_notification_logs_created_at ON notification_logs(created_at DESC);
CREATE INDEX idx_notification_logs_status ON notification_logs(status);

